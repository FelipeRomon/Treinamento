/**
 * Classe para exemplificar o funcionamento do metodo startWith
 * @author fcorrea
 * @since 17/02/2020
 * @version 0.1
 */
public class VerificaPrefixo {

	public static void main(String[] args) {
		
		String str = "11 976090787";
		
		// Estraindo o inicio da String
		if (str.startsWith("11")) {
			
			System.out.println("Região de São Paulo.");
			
		}
	}
}
